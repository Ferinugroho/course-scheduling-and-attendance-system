<h3 align="center">  Visi </h3> <br>
Visi Program Studi Manajemen Informatika adalah menjadi program studi pendidikan vokasi yang unggul dalam menyiapkan Sumber Daya Manusia di bidang Manajemen Informatika.
 </center> <br>
<h3 align="center">Misi</h3><br>
<p align="left">
  Untuk mewujudkan visi Program Studi Manajemen Informatika maka dilakukan usaha/upaya sebagai misi program studi, yaitu:<br>
  <ol>
<li>Meningkatkan penyelenggaraan pendidikan vokasi dalam bidang Manajemen Informatika yang berkualitas.</li>
<li>Meningkatkan pengetahuan dan keterampilan sumber daya manusia sesuai kompetensi yang akan dicapai.</li>
<li>Mengembangkan organisasi dan meningkatkan mutu pengelolaan sumber daya di Jurusan Manajemen Informatika untuk mewujudkan     
kinerja secara efektif, efisien dan berkelanjutan.</li>
<li>Meningkatkan kerjasama dengan pihak lain di bidang Manajemen Informatika yang saling menguntungkan dalam rangka peningkatan  
mutu penyelenggaraan Tri Dharma Perguruan Tinggi.</li>
   </ol>
</p>
